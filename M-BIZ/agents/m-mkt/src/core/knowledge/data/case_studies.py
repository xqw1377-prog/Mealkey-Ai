from ..case import CaseStudy, DecisionRule

ALL_CASES: list[CaseStudy] = []
_CASE_MAP: dict[str, CaseStudy] = {}


def _register(c: CaseStudy) -> CaseStudy:
    ALL_CASES.append(c)
    _CASE_MAP[c.name] = c
    return c


def get_case(name: str) -> CaseStudy | None:
    return _CASE_MAP.get(name)


