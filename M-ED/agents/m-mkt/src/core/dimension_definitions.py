SIX_DIMENSIONS = [
    DIM_MARKET_CAPACITY,
    DIM_COMPETITIVE_LANDSCAPE,
    DIM_CONSUMER_FIT,
    DIM_OPERATIONAL_FEASIBILITY,
    DIM_BRAND_MOMENTUM,
    DIM_ENVIRONMENTAL_FIT,
]


def register_all() -> None:
    """
    将所有六个维度注册到 DimensionRegistry

    在项目初始化时调用一次即可。

    Example:
        from core.dimension_definitions import register_all
        register_all()
        model = DimensionRegistry.build_model(name="某城市评估")
    """
    DimensionRegistry.clear()
    for dim in SIX_DIMENSIONS:
        DimensionRegistry.register(dim)


def build_opportunity_model(
    name: str = "餐饮市场机会评估", description: str = ""
) -> SixDimensionModel:
    """
    一键构建完整的六维模型（含六个维度及其预设指标）

    Args:
        name: 模型名称
        description: 模型描述

    Returns:
        SixDimensionModel: 完整的六维模型，含维度定义与指标结构
    """
    register_all()
    return DimensionRegistry.build_model(name=name, description=description)
