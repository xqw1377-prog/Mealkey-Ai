export function getCreativeTemplate(
  type: string,
  category: string,
  city: string,
): CreativeTemplate {
  for (const [key, factory] of Object.entries(templates)) {
    if (type.includes(key) || key.includes(type)) {
      return factory(category, city);
    }
  }
  return defaultTemplate(category, city);
}
